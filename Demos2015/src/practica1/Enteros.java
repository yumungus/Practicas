
package practica1;

/**
 * Enteros.java
 * Programa para mostrar constantes predefinidas en envoltorio Integer.
 * ajp - 2015.09.20
 */

public class Enteros {
	public static void main(String[] args) {
		
			System.out.println("Valor máximo tipo byte: " + Byte.MAX_VALUE);
			System.out.println("Valor mínimo tipo byte: " + Byte.MIN_VALUE);
			
			System.out.println("Valor máximo tipo byte: " + Integer.MAX_VALUE);
			System.out.println("Valor mínimo tipo byte: " + Integer.MIN_VALUE);
			
			System.out.println("Valor máximo tipo double: " + Double.MAX_VALUE);
			System.out.println("Valor mínimo tipo double: " + Double.MIN_VALUE);
			System.out.println("Valor máximo exponente double: " + Double.MAX_EXPONENT);
			System.out.println("Valor mínimo exponente double: " + Double.MIN_EXPONENT);
			System.out.println("Valor infinito tipo double: " + Double.POSITIVE_INFINITY);
			System.out.println("Valor máximo tipo long: " + Long.MAX_VALUE);
			
			
	}

}
